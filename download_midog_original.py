from urllib.request import urlretrieve
import os
from tqdm import tqdm

# Create dataset directory structure
dataset_dir = 'datasets'
midog_original_dir = os.path.join(dataset_dir, 'midog_original')
images_dir = os.path.join(midog_original_dir, 'images')
metadata_dir = os.path.join(midog_original_dir, 'metadata')

# Create directories if they don't exist
os.makedirs(images_dir, exist_ok=True)
os.makedirs(metadata_dir, exist_ok=True)

print(f"Creating MIDOG dataset directories...")
print(f"- {images_dir}")
print(f"- {metadata_dir}")

for file in tqdm(filename, desc="Downloading MIDOG files"):
    # Determine target directory based on file type
    if filename[file].endswith('.json') or filename[file].endswith('.sqlite'):
        target_dir = metadata_dir
    else:
        target_dir = images_dir
    
    file_path = os.path.join(target_dir, filename[file])
    
    # Check if file already exists
    if os.path.exists(file_path):
        print(f"File {filename[file]} already exists, skipping...")
        continue
    
    url = f'https://figshare.com/ndownloader/files/{file}'
    urlretrieve(url, file_path)

print(f"\nDownload completed!")
print(f"TIFF images: {images_dir}")
print(f"Metadata files: {metadata_dir}")
